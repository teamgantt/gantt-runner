    init = function(self,  x,  y,  template_idx)

      self.x = x
      self.y = y
      self.template_idx = template_idx
      self.template = templates[template_idx]
      self.milestones = {}
      self.bars = {}
      self.dep_lines={}
      self.can_remove = false

      -- setup bars
      for y=1, 16 do
        for x=1, 16 do
          local tile = self.template[y][x]
          local next_tile = nil
          local prev_tile = nil
          local new_x = (x-1)*8+self.x
          local new_y = (y-1)*8+self.y

          if (x > 1) then
            prev_tile = self.template[y][x-1]
          end
          if (x < 16) then
            next_tile = self.template[y][x+1]
          end

          if (tile == 5) then
            add(self.milestones,  {
              x=new_x,
              y=new_y,
              animate=tick_frames,
              f=1
            })
            g.total_milestones += 1
          elseif (flr(tile) == 7) then
            -- draw dependency lines #7.x
            add(self.dep_lines,  {
              s=deps[tostr(tile)],
              x=new_x,
              y=new_y,
            })
          elseif (tile > 0) then
            -- draw proper bar tiles
            if (prev_tile == 0 or prev_tile == nil or flr(prev_tile) == 7) then
              add(self.bars,  {
                s=pcs[tile][1],
                x=new_x,
                y=new_y,
              })

            elseif (next_tile == tile) then
              add(self.bars,  {
                s=pcs[tile][2],
                x=new_x,
                y=new_y,
              })

            else
              add(self.bars,  {
                s=pcs[tile][3],
                x=new_x,
                y=new_y,
              })
            end
          end
        end
      end
    end,


    update = function(self)
      -- update milestones
      for i, m in pairs(self.milestones) do
        m:animate()
      end
    end,

    setup_milestones = function(self)
      -- setup milestones
      for y=1, 16 do
        for x=1, 16 do
          local tile = self.template[y][x]
          if (tile == 5) then
            -- milestone
            add(self.milestones,  {
              x=(x-1)*8+self.x,
              y=(y-1)*8+self.y,
              animate=tick_frames,
              f=1
            })
            g.total_milestones += 1

          end
        end
      end
    end,

    milestone_collide = milestone_collide,

    draw = function(self)
      -- draw bars
      for k, bar in pairs(self.bars) do
        spr(bar.s,  bar.x,  bar.y)
      end

      -- draw dependency lines
      for k, line in pairs(self.dep_lines) do
        spr(line.s,  line.x,  line.y)
      end

      -- draw milestones
      for k, mile in pairs(self.milestones) do
        spr(mile_frames[flr(mile.f)],  mile.x,  mile.y)
      end
    end
  }
